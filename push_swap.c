#include "push_swap.h"

int	main(int ac, char **av)
{
	if (ac >= 2)
	{
		while (ac >= 2)
		{
			if (is_int(av[ac - 1]))
			{
				ft_printf("Int correct : '%s'\n", av[ac - 1]);

			}
			ac--;
		}
	}
	return (0);
}
